# Инициализируем пустой список
user_inputs = []

# Запрашиваем ввод у пользователя 10 раз
for i in range(10):
    user_input = input(f"Введите символ или цифру {i + 1}: ")
    user_inputs.append(user_input)  # Добавляем введенное значение в список

# Выводим полученный список
print("Вы ввели следующие символы/цифры:")
print(user_inputs)